public class StringMethod{
	public static void main(String[] args){
		String edu = "Hsih-Hsin";
		System.out.println("字串內容:"+edu);
		System.out.println("字串長度:"+edu.length());
		System.out.println("等於hsih-hsin?"+edu.equals("hsih-hsin"));
		System.out.println("大寫 " +edu.toUpperCase());
		System.out.println("小寫:" +edu.toLowerCase());
	}
}
